const int mod = 1e9+7;
const int MX = 100;
int k, d;
int dp[MX+5][2];
int calc(int num, bool ok)
{
    // if(at==k){
        // if(num == 0 && ok) return 1;
        // else return 0;
    // }
    if(num == 0){
        if(ok) return 1;
        else return 0;
    }
    
    if(dp[num][ok] != -1) return dp[num][ok];
    
    int ans = 0;
    for(int K = 1; K <= k; K++){
        if(K > num) return dp[num][ok] = ans;
        ans = (ans+calc(num-K, (K>=d ? ok|1 : ok)))%mod;
    }
    
    return dp[num][ok] = ans;
}
